# Test connection to remote TWS
test_remote_connection() {
    log_info "Testing connection to remote TWS..."

    # Source the configuration to get IP and ports
    if [ -f "$CONFIG_DIR/config_remote_tws.py" ]; then
        # Extract IP and ports from config file
