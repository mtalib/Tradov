echo "-----------------"
echo "Memory: $(free -h | awk '/^Mem:/ {printf "%s / %s (%.1f%%)", $3, $2, $3/$2*100}')"
echo "CPU Load: $(uptime | awk -F'load average:' '{print $2}')"
