du -sh "$LOG_DIR" 2>/dev/null | awk '{print "  Logs: " $1}'
du -sh "$DATA_DIR" 2>/dev/null | awk '{print "  Data: " $1}'

echo ""
echo "Recent Errors (last 5):"
echo "-----------------------"
if [ -d "$LOG_DIR" ]; then
    ERRORS=$(grep -i "error\|critical" "$LOG_DIR/system/"*.log 2>/dev/null | tail -5)
    if [ -n "$ERRORS" ]; then
        echo "$ERRORS" | while IFS= read -r line; do
            echo "  ${line##*/}"
        done
    else
        echo "  No recent errors"
    fi
else
