def get_active_config():
    """Get configuration for active trading mode with remote TWS settings"""
    # For remote TWS, we don't need auth credentials in the same way
    # TWS handles authentication on the Windows computer

    paper_config = IB_CONFIG["gateway"]["paper"]
    live_config = IB_CONFIG["gateway"]["live"]

    # Default to paper for initial setup
    mode = os.environ.get("TRADING_MODE", "paper")

    if mode == "paper":
        return {
            "host": paper_config["host"],
            "port": paper_config["port"],
            "clientId": paper_config["clientId"],
            "mode": "paper",
            "connection_type": "remote_tws",
            "remote_ip": "$windows_ip",
        }
    else:
        return {
            "host": live_config["host"],
            "port": live_config["port"],
            "clientId": live_config["clientId"],
            "mode": "live",
            "connection_type": "remote_tws",
            "remote_ip": "$windows_ip",
        }

def validate_remote_connection():
    """Validate remote TWS connection"""
    import socket
    import time

    config = get_active_config()
    host = config["host"]
    port = config["port"]

    try:
        # Test TCP connectivity
        sock = socket.socket(socket.AF_INET, socket.SOCK_STREAM)
        sock.settimeout(10)
        result = sock.connect_ex((host, port))
        sock.close()

        if result == 0:
            return True, f"Connection to {host}:{port} successful"
        else:
            return False, f"Cannot connect to {host}:{port} (error: {result})"

    except Exception as e:
        return False, f"Connection test failed: {str(e)}"

