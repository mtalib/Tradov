        local paper_port=$(grep 'port.*:.*#.*Paper' "$CONFIG_DIR/config_remote_tws.py" | grep -o '[0-9]*')
        local live_port=$(grep 'port.*:.*#.*Live' "$CONFIG_DIR/config_remote_tws.py" | grep -o '[0-9]*')

        if [ -n "$windows_ip" ] && [ -n "$paper_port" ]; then
            log_info "Testing connection to $windows_ip:$paper_port (paper trading)..."
            test_network_connectivity "$windows_ip" "$paper_port"
        else
            log_error "Could not extract connection details from configuration"
            return 1
        fi
    else
        log_error "Remote TWS configuration not found. Run setup first."
        return 1
    fi
}

# Interactive configuration mode
interactive_setup() {
    echo -e "${BLUE}╔══════════════════════════════════════════════════════════════════════════════╗"
    echo -e "║                        INTERACTIVE SETUP MODE                               ║"
    echo -e "╚══════════════════════════════════════════════════════════════════════════════╝${NC}"
    echo ""

    # Get Windows computer IP
    while true; do
        echo -e "${YELLOW}Enter the IP address of your Windows computer running TWS:${NC}"
        echo -e "${BLUE}  (You can find this by running 'ipconfig' on Windows)${NC}"
        read -p "IP Address: " windows_ip

        if validate_ip "$windows_ip"; then
            log_success "Valid IP address: $windows_ip"
            break
        else
            log_error "Invalid IP address format. Please enter a valid IPv4 address."
        fi
    done

    # Get paper trading port
    echo ""
    echo -e "${YELLOW}Enter the paper trading port (default 7497 for TWS):${NC}"
    read -p "Paper Port [7497]: " paper_port
    paper_port=${paper_port:-7497}

    # Get live trading port
    echo ""
    echo -e "${YELLOW}Enter the live trading port (default 7496 for TWS):${NC}"
    read -p "Live Port [7496]: " live_port
    live_port=${live_port:-7496}

    # Test connectivity
    echo ""
    log_info "Testing connectivity to Windows computer..."
    if test_network_connectivity "$windows_ip" "$paper_port"; then
        log_success "Connection test passed!"
    else
        echo ""
        log_warning "Connection test failed. Common solutions:"
        echo -e "${BLUE}  1. Make sure TWS is running on Windows computer${NC}"
        echo -e "${BLUE}  2. Enable API in TWS: API -> Settings -> Enable ActiveX and Socket Clients${NC}"
        echo -e "${BLUE}  3. Add this Ubuntu computer's IP to TWS Trusted IPs${NC}"
        echo -e "${BLUE}  4. Check Windows firewall settings${NC}"
        echo ""

        while true; do
            read -p "Continue with setup anyway? (y/n): " continue_setup
            case $continue_setup in
                [Yy]* ) break;;
                [Nn]* )
                    log_info "Setup cancelled. Fix connection issues and try again."
                    exit 1
                    ;;
                * ) echo "Please answer yes or no.";;
            esac
        done
    fi

    # Create configuration
    echo ""
    log_info "Creating remote TWS configuration..."
    create_remote_config "$windows_ip" "$paper_port" "$live_port"

    # Offer to switch to new config
    echo ""
    while true; do
        read -p "Switch to the new remote TWS configuration now? (y/n): " switch_config
        case $switch_config in
            [Yy]* )
                switch_to_remote_config
                break
                ;;
            [Nn]* )
                log_info "Configuration created but not activated."
                log_info "Run '$0 --switch-to-remote' to activate later."
                break
                ;;
            * ) echo "Please answer yes or no.";;
        esac
    done

    show_next_steps
}

# Switch to remote configuration
switch_to_remote_config() {
    if [ -f "$CONFIG_DIR/config_remote_tws.py" ]; then
        # Backup current config
        backup_config

        # Switch to remote config
        cp "$CONFIG_DIR/config_remote_tws.py" "$CONFIG_DIR/config.py"
        log_success "Switched to remote TWS configuration"

        # Update any hardcoded localhost references in your codebase
        update_localhost_references
    else
        log_error "Remote TWS configuration not found. Run setup first."
        exit 1
    fi
}

# Update localhost references in codebase
update_localhost_references() {
    log_info "Updating localhost references in codebase..."

    # Get the Windows IP from config
    local windows_ip=$(grep 'ip_address.*:' "$CONFIG_DIR/config.py" | sed 's/.*: *"\([^"]*\)".*/\1/')

    if [ -n "$windows_ip" ]; then
        # Find and update common localhost references (but be conservative)
        log_info "Found Windows IP: $windows_ip"
        log_info "Note: Manual code updates may be needed for hardcoded '127.0.0.1' references"

        # Create a summary of files that might need updates
        echo ""
        log_warning "Files that may need manual updates:"
        grep -r "127\.0\.0\.1" --include="*.py" "$SCRIPT_DIR" | head -10 | while read line; do
            echo -e "${YELLOW}  $line${NC}"
        done
    fi
}

# Revert to local Gateway configuration
revert_to_local() {
    log_info "Reverting to local Gateway configuration..."

    if [ -f "$CONFIG_DIR/backups" ]; then
        # Find most recent backup
        local latest_backup=$(ls -t "$CONFIG_DIR/backups/config_"*.py 2>/dev/null | head -1)
        if [ -n "$latest_backup" ]; then
            cp "$latest_backup" "$CONFIG_DIR/config.py"
            log_success "Reverted to: $latest_backup"
        else
            log_error "No backup found. Please restore configuration manually."
        fi
    else
        log_error "No backup directory found. Cannot revert automatically."
    fi
}

