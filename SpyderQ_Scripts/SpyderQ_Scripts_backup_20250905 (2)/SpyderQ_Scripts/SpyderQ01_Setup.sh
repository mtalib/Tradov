#!/bin/bash
# ===============================================================================
# SPYDER - Autonomous Options Trading System
#
# Script: SpyderQ11_StopAll.sh
# Group: Q (Scripts/Shell)
# Purpose: Stop all Spyder Gateway components gracefully
# Author: Mohamed Talib
# Date Created: 2025-08-03
# Last Updated: 2025-08-03 Time: 15:30:00
#
# Description:
#     Stops all monitoring components in reverse order with proper cleanup.
#     Ensures graceful shutdown and removes PID files.
# ===============================================================================

SPYDER_HOME="/home/adam/Projects/Spyder"
LOG_DIR="$SPYDER_HOME/logs"

GREEN='\033[0;32m'
RED='\033[0;31m'
YELLOW='\033[1;33m'
NC='\033[0m'

echo "================================================"
echo "  SPYDER Gateway - Stopping All Components"
echo "  Script: SpyderQ11_StopAll.sh"
echo "================================================"

stop_component() {
    local name=$1
    local pidfile=$2
    
    if [ -f "$pidfile" ]; then
        PID=$(cat "$pidfile")
        if ps -p $PID > /dev/null 2>&1; then
            echo "Stopping $name (PID: $PID)..."
            kill -TERM $PID 2>/dev/null
            sleep 2
            
            # Force kill if still running
            if ps -p $PID > /dev/null 2>&1; then
                echo -e "${YELLOW}[!]${NC} Force stopping $name"
                kill -KILL $PID 2>/dev/null
            fi
            
            echo -e "${GREEN}[✓]${NC} $name stopped"
            rm -f "$pidfile"
        else
            echo -e "${YELLOW}[!]${NC} $name not running (stale PID file)"
            rm -f "$pidfile"
        fi
    else
        echo -e "${YELLOW}[!]${NC} $name not running (no PID file)"
    fi
}

# Stop components in reverse order
echo ""
stop_component "Integration Manager" "$LOG_DIR/integration.pid"
stop_component "Multi-Client Watchdog" "$LOG_DIR/watchdog.pid"
stop_component "Prometheus Metrics" "$LOG_DIR/metrics.pid"

echo ""
echo "================================================"
echo -e "${GREEN}All components stopped${NC}"
echo "================================================"
EOF

chmod +x "$SCRIPTS_DIR/SpyderQ11_StopAll.sh"

