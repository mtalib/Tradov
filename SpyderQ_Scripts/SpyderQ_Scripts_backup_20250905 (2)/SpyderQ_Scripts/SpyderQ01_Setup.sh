                    echo -e "  ${RED}[✗]${NC} Client $CLIENT_ID disconnected"
                fi
            fi
        fi
    done
else
    echo -e "${RED}[✗]${NC} Metrics endpoint not responding"
fi

echo ""
echo "================================================"
EOF

