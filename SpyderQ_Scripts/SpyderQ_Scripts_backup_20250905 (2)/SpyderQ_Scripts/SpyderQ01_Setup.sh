check_component() {
    local name=$1
    local pidfile=$2
    
    if [ -f "$pidfile" ]; then
        PID=$(cat "$pidfile")
        if ps -p $PID > /dev/null 2>&1; then
            # Get memory usage
            MEM=$(ps -o rss= -p $PID | awk '{printf "%.1f", $1/1024}')
            echo -e "${GREEN}[✓]${NC} $name (PID: $PID, Mem: ${MEM}MB)"
            return 0
        else
            echo -e "${RED}[✗]${NC} $name (stale PID)"
            return 1
        fi
    else
        echo -e "${RED}[✗]${NC} $name"
        return 1
    fi
}

