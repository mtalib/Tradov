echo "Component Status:"
echo "-----------------"

# Check IB Gateway
if pgrep -f "ibgateway" > /dev/null; then
    GATEWAY_PID=$(pgrep -f "ibgateway" | head -1)
    echo -e "${GREEN}[✓]${NC} IB Gateway (PID: $GATEWAY_PID)"
