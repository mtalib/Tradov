# Check if IP address is valid
validate_ip() {
    local ip=$1
    if [[ $ip =~ ^[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}$ ]]; then
        IFS='.' read -ra ADDR <<< "$ip"
        for i in "${ADDR[@]}"; do
            if [[ $i -gt 255 ]]; then
                return 1
            fi
        done
        return 0
    fi
    return 1
}

# Test network connectivity to Windows machine
test_network_connectivity() {
    local ip=$1
    local port=$2

    log_info "Testing network connectivity to $ip:$port..."

    # Test ping first
    if ping -c 1 -W 3 "$ip" > /dev/null 2>&1; then
        log_success "Network connectivity to $ip: OK"
    else
        log_warning "Ping to $ip failed - but this might be normal (Windows firewall)"
    fi

    # Test port connectivity
    if timeout 5 bash -c "cat < /dev/null > /dev/tcp/$ip/$port" 2>/dev/null; then
        log_success "Port $port is accessible on $ip"
        return 0
    else
        log_error "Port $port is NOT accessible on $ip"
        log_info "Make sure TWS is running and API is enabled on the Windows computer"
        return 1
    fi
}

# Backup current configuration
backup_config() {
