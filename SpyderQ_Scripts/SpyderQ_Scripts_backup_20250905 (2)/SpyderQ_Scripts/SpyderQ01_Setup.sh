check_component "Prometheus Metrics" "$LOG_DIR/metrics.pid"
check_component "Multi-Client Watchdog" "$LOG_DIR/watchdog.pid"
check_component "Integration Manager" "$LOG_DIR/integration.pid"

echo ""
echo "Network Ports:"
echo "--------------"

check_port() {
    local port=$1
    local name=$2
    if netstat -tuln 2>/dev/null | grep -q ":$port "; then
        echo -e "${GREEN}[✓]${NC} Port $port ($name)"
    else
        echo -e "${RED}[✗]${NC} Port $port ($name)"
    fi
}

